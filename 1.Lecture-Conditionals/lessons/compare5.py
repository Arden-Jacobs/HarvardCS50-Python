# function is used to prompt the user for values of x and y, which are converted to integers.The program uses
# an if statement with the inequality operator (!=) to check if x is not equal to y. If this condition is true,
# it means that x is not equal to y, and the corresponding message is printed. Otherwise, if the condition is
# false, it implies that x is equal to y, and the corresponding message is printed.