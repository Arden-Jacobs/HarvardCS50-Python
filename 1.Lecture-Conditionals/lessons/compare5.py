# compare5.py - Demonstrates Inequality

# Prompt the user for values of x and y, and convert them to integers.
x = int(input("What's x? "))
y = int(input("What's y? "))

# Compare x and y using inequality to determine whether they are not equal.

# If x is not equal to y, print the corresponding message.
if x != y:
    print("x is not equal to y")

# If x is equal to y, print the corresponding message.
else:
    print("x is equal to y")



