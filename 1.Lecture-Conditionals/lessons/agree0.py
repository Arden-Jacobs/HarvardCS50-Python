# agree0.py - Compares Strings

# Prompt the user for input and store it in the variable `answer`.
answer = input("Do you agree? ")

# Compare the value of `answer` with the string "yes" to determine agreement.

# If `answer` is equal to "yes", print the corresponding message.
if answer == "yes":
    print("Agreed")

# If `answer` is not equal to "yes", print the corresponding message.
else:
    print("Not agreed")



# In this section, the program demonstrates comparing strings. The input() function is used to prompt the user
# for input, which is stored in the variable answer. The program then uses an if statement to compare the value
# of answer with the string "yes". If the condition is true (meaning the user entered "yes"),the program
# prints "Agreed". If the condition is false (meaning the user did not enter "yes"),
# the program prints"Not agreed".