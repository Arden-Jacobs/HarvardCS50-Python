# compare3.py - Demonstrates Inequalities and Logical Operator

# Prompt the user for values of x and y, and convert them to integers.
x = int(input("What's x? "))
y = int(input("What's y? "))

# Compare x and y using inequalities and the logical operator to determine the relationship between them.

# If x is less than y or x is greater than y, print the corresponding message.
if x < y or x > y:
    print("x is not equal to y")

# If neither of the above conditions is met, it implies that x must be equal to y.
else:
    print("x is equal to y")



