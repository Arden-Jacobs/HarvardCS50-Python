# compare4.py - Demonstrates Equality

# Prompt the user for values of x and y, and convert them to integers.
x = int(input("What's x? "))
y = int(input("What's y? "))

# Compare x and y using equality to determine whether they are equal.

# If x is equal to y, print the corresponding message.
if x == y:
    print("x is equal to y")

# If x is not equal to y, print the corresponding message.
else:
    print("x is not equal to y")



# In this section, the program demonstrates the use of equality to compare values of x and y.The input() function
# is used to prompt the user for values of x and y, which are converted to integers. The program uses an if
# statement with the equality operator (==) to check if x is equal to y. If this condition is true, it means
# that x is equal to y, and the corresponding message is printed. Otherwise, if the condition is false, it
# implies that x is not equal to y, and the corresponding message is printed.