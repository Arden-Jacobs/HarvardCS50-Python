# compare0.py - Demonstrates Conditionals

# Prompt the user for values of x and y, and convert them to integers.
x = int(input("What's x? "))
y = int(input("What's y? "))

# Compare x and y using if statements to determine the relationship between them.

# If x is less than y, print the corresponding message.
if x < y:
    print("x is less than y")

# If x is greater than y, print the corresponding message.
if x > y:
    print("x is greater than y")

# If x is equal to y, print the corresponding message.
if x == y:
    print("x is equal to y")



