# Gets a number from the user

# Prompt the user for input and store it in variable x
x = int(input("What's x? "))

# Print the value of x using an f-string
print(f"x is {x}")



# Explanation:
# The code begins with a comment indicating its purpose, which is to get a number from the user and
# display it.

# The input() function is used to prompt the user with the question "What's x? ". The user's input is
# then captured and stored in the variable x.

# The int() function is used to convert the user input (which is a string) into an integer. This allows
# the program to perform numerical operations with the input.

# The print() function is used to display a message that includes the value of x. The f before the
# string indicates that it is an f-string, allowing variables to be included within curly braces {}.

# In the curly braces within the f-string, x is placed to display the value of the variable.

# In summary, this code snippet prompts the user for a number, converts the user's input into an
# integer, and then displays a message indicating the value of x.
