# names0.py - Stores a name in a variable and prints a greeting message

# Prompt the user to input their name and store it in the "name" variable
name = input("What's your name? ")

# Print a greeting message using the stored name
print(f"hello, {name}")

# Explanation:
# This script prompts the user to input their name using the input() function and stores the input in the "name" variable.
# The script then uses the f-string (formatted string) syntax to print a greeting message that includes the stored name.
# The message "hello, {name}" is printed to the console, where {name} is replaced with the actual value stored in the "name" variable.
