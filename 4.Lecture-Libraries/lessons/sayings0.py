# sayings0.py - Custom module for generating messages

# Define a function to print a greeting message
def hello(name):
    print(f"hello, {name}")

# Define a function to print a goodbye message
def goodbye(name):
    print(f"goodbye, {name}")

# Explanation:
# This custom module defines two functions for generating greeting and goodbye messages.

# The function "hello(name)" takes a name as an argument and prints a message using an f-string,
# welcoming the provided name with "hello."

# The function "goodbye(name)" takes a name as an argument and prints a message using an f-string,
# saying goodbye to the provided name with "goodbye."
