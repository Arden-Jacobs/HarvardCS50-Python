# hello2.py - String Concatenation

# This program showcases how to take user input and perform string concatenation
# to create a personalized greeting message.

# The `input()` function prompts the user for their name and stores it in the variable `name`.
name = input("What's your name? ")

# The `print()` function outputs a personalized greeting by concatenating
# the string "hello, " with the value stored in the `name` variable.
print("hello, " + name)



