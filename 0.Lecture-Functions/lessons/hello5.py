# hello5.py - String Formatting with f-strings

# This program demonstrates how to use f-strings for string formatting,
# allowing us to easily insert variables into strings.

# The `input()` function prompts the user for their name and stores it in the variable `name`.
name = input("What's your name? ")

# The `print()` function uses an f-string to create a personalized greeting.
# The value of the `name` variable is inserted into the string using curly braces.
print(f"hello, {name}")



# In this section, we're introducing the concept of f-strings for string formatting. The user's name 
# is obtained using the input() function and stored in the name variable. The print() function uses 
# an f-string to create a personalized greeting, where the value of the name variable is inserted 
# into the string using curly braces.