# calculator1.py - User Input and Concatenation

# This program demonstrates user input and concatenation of strings.

# The `input()` function is used to prompt the user for the value of `x` and store it in the variable `x`.
x = input("What's x? ")

# Similarly, the `input()` function is used to prompt the user for the value of `y` and store it in the variable `y`.
y = input("What's y? ")

# Variables `x` and `y` store string values obtained from user input.
# The `+` operator is used to concatenate these strings, resulting in `z` containing their concatenated value.
z = x + y

# The `print()` function is used to display the concatenated value of `z`.
print(z)



# In this section, we're introducing user input and string concatenation. The input() function is used twice
# to prompt the user for values of x and y, which are stored as string variables. The + operator is then used
#  to concatenate the strings stored in x and y, and the result is stored in the variable z. The print() 
# function is used to display the concatenated value of z.