# calculator4.py - User Input and Floating-Point Addition

# This program demonstrates user input, type conversion to floating-point numbers,
# and addition of floating-point numbers.

# The `float()` function is used to convert the user's input directly into floating-point numbers.
# The `input()` function is used to prompt the user for the value of `x` and convert it to a float, stored in the variable `x`.
x = float(input("What's x? "))

# Similarly, the `input()` function is used to prompt the user for the value of `y` and convert it to a float, stored in the variable `y`.
y = float(input("What's y? "))

# The variables `x` and `y` store float values obtained from user input.
# The float numbers are added using the `+` operator, and the result is stored in the variable `z`.
z = x + y

# The `print()` function is used to display the float value of `z`.
print(z)



# In this section, we're demonstrating user input, type conversion to floating-point numbers, and addition
# of floating-point numbers. The float() function is used to directly convert the user's input into 
# floating-point numbers. The input() function is used twice to prompt the user for values of x and y,
#  and the resulting float values are used for calculations. The print() function is used to display 
# the float value of z.