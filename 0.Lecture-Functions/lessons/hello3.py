# hello3.py - Print Formatting

# This program demonstrates an alternative way to create a personalized greeting
# using the `print()` function's formatting capabilities.

# The `input()` function prompts the user for their name and stores it in the variable `name`.
name = input("What's your name? ")

# The `print()` function is used to output a personalized greeting.
# The comma in the `print()` statement automatically adds a space between "hello," and the value stored in the `name` variable.
print("hello,", name)



