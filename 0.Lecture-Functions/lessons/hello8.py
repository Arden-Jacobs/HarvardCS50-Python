# hello8.py - Functions and User Input

# This program demonstrates the use of functions and user input.

def hello():
    """
    Prints a greeting message "hello".
    """
    print("hello")

# The `input()` function is used to prompt the user for their name and store it in the variable `name`.
name = input("What's your name? ")

# The `hello()` function is called to print "hello".
hello()

# The value stored in the variable `name` is printed using the `print()` function.
print(name)



