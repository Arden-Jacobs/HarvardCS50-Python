# calculator2.py - User Input and Integer Addition

# This program demonstrates user input, type conversion, and integer addition.

# The `input()` function is used to prompt the user for the value of `x` and store it in the variable `x`.
x = input("What's x? ")

# Similarly, the `input()` function is used to prompt the user for the value of `y` and store it in the variable `y`.
y = input("What's y? ")

# The `int()` function is used to convert the string values of `x` and `y` into integers.
# The integers are then added using the `+` operator, and the result is stored in the variable `z`.
z = int(x) + int(y)

# The `print()` function is used to display the integer value of `z`.
print(z)



# In this section, we're introducing type conversion and integer addition. The input() function is used twice 
# to prompt the user for values of x and y, which are initially stored as strings. The int() function is then 
# used to convert these strings into integer values. The integers are added using the + operator, and the 
# result is stored in the variable z. The print() function is used to display the integer value of z.