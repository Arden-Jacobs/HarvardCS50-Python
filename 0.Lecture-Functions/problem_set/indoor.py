# indoor.py - User Input and Lowercase Conversion

# Prompt the user for input and store it in the variable `prompt`.
prompt = input("Enter something you would like to echo: ")

# Convert the content of the `prompt` variable to lowercase using the `lower()` method,
# and print the result.
print(prompt.lower())



# In this section, the program prompts the user for input using the input() function and stores it in
# the variable prompt. The content of the prompt variable is then converted to lowercase using the 
# lower() method, and the result is printed using the print() function.