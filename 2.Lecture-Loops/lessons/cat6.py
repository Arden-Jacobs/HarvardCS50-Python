# cat6.py - Demonstrates a For Loop Using the range Function

# Use a for loop to iterate using the range function
for i in range(3):
    print("meow")



# Explanation:
# This code demonstrates the utilization of a for loop with the range function to facilitate iteration through
# a sequence of numbers.

# For Loop with range:
# The for loop is employed in conjunction with the range(3) function, which generates a sequence of numbers from 0
# up to (but not including) 3.During each iteration, the variable i takes on the values from the generated sequence.

# Loop Body:

# Inside the loop, the print("meow") statement is executed. This results in the string "meow" being printed to the
# console during each iteration of the loop.

# Loop Iteration:
# The loop iterates three times, in correspondence with the generated sequence of numbers [0, 1, 2].
