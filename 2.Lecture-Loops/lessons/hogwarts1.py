# hogwarts1.py - Demonstrating Iterating Over a List

# List of students' names
students = ["Hermione", "Harry", "Ron"]

# Iterate over the list and print each student's name
for student in students:
    print(student)



# Explanation:
# This code demonstrates how to iterate over a list and perform an action (printing in this case) on each
# element using a for loop.

# List Definition:
# The students list is defined with three strings representing students' names: "Hermione", "Harry", and "Ron".

# Iterating Over the List:
# The for loop iterates over each element in the students list.
# In each iteration, the current element (student's name) is assigned to the variable student.
# The print(student) statement then prints the current student's name.
# The loop iterates through each element of the students list, printing each student's name one by one.
