# hogwarts3.py - Demonstrating Indexing into a Dictionary

# Dictionary of students' houses
students = {
    "Hermione": "Gryffindor",
    "Harry": "Gryffindor",
    "Ron": "Gryffindor",
    "Draco": "Slytherin",
}

# Accessing and printing houses of specific students using keys
print(students["Hermione"])
print(students["Harry"])
print(students["Ron"])
print(students["Draco"])



# Explanation:
# This code demonstrates how to access and retrieve values from a dictionary using keys.

# Dictionary Definition:
# The students dictionary is defined with student names as keys and their corresponding houses as values.
# Each key-value pair represents a student's name and their assigned house in Hogwarts.

# Indexing into the Dictionary:
# The print statements demonstrate how to access values from the dictionary using specific keys.
# For example, students["Hermione"] retrieves the value associated with the key "Hermione," which is "Gryffindor."
# By indexing into the dictionary using specific keys, the code prints the houses of individual students
