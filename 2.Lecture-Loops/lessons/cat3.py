# cat3.py - Demonstrates a While Loop for Counting Up

# Initialize the counter
i = 0

# Use a while loop to count up
while i < 3:
    print("meow")
    i = i + 1



# Explanation:
# This code illustrates the use of a while loop to perform a certain action multiple times while a specific
# condition is met. Here, the loop is employed to count up from a starting value of 0.

# Initialize Counter:
# The variable i is initialized with the value 0. This variable is used to keep track of the counting process.

# While Loop:
# The while loop is employed to repeatedly execute a block of code as long as the specified condition (i < 3) is true.
# The loop will continue executing as long as the value of i is less than 3.

# Loop Body:
# Inside the loop, the print("meow") statement is executed. This results in printing the string "meow" to
# the console during each iteration of the loop.

# Update Counter:
# After each iteration of the loop, the value of i is increased by 1 using the statement i = i + 1.
# This incrementation effectively advances the count.

# Loop Termination:
# The loop will persist in iterating as long as the condition i < 3 remains true. Once i becomes equal to or
# greater than 3, the condition becomes false, and the loop terminates.
