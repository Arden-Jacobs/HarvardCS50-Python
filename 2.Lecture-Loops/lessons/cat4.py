# cat4.py - Demonstrates a While Loop for Counting Up

# Initialize the counter
i = 0

# Use a while loop to count up
while i < 3:
    print("meow")
    i += 1



# Explanation:
# This code demonstrates the use of a while loop to perform a specific action multiple times while a certain
# condition is satisfied. In this instance, the loop is utilized to count up from an initial value of 0.

# Initialize Counter:
# The variable i is initialized with the value 0. This variable tracks the counting process.

# While Loop:
# The while loop repeatedly executes a block of code as long as the specified condition (i < 3) holds true.
# The loop continues executing as long as the value of i remains less than 3.

# Loop Body:
# Inside the loop, the print("meow") statement is executed. This results in the string "meow" being printed
# to the console during each iteration of the loop.

# Update Counter:
# After each iteration of the loop, the value of i is incremented by 1 using the statement i += 1.
# This shorthand notation serves to advance the count.

# Loop Termination:
# The loop continues to iterate as long as the condition i < 3 remains true. When i becomes equal to or
# greater than 3, the condition becomes false, and the loop terminates.
