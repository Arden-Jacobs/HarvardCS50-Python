# cat8.py - Demonstrates String Multiplication

# Print the string "meow" three times using string multiplication
print("meow\n" * 3, end="")



# Explanation:
# This code showcases the concept of string multiplication, which involves repeating a string a certain number of times.

# String Multiplication:
# The expression "meow\n" * 3 involves multiplying the string "meow\n" by the integer 3. This results in the string
# being repeated three times, with newline characters (\n) between each repetition.

# Print Function:
# The print() function is utilized to output the result of the string multiplication to the console.
# The end="" parameter is included to prevent the print() function from automatically appending a newline 
# character at the end of the output.
