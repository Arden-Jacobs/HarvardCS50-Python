# cat1.py - Demonstrates a While Loop for Counting Down

# Initialize the counter
i = 3

# Use a while loop to count down
while i != 0:
    print("meow")
    i = i - 1



# Explanation:
# This code demonstrates the use of a while loop to perform an action multiple times while a condition is met.
# The loop in this case is used to count down from a starting value of 3.

# Initialize Counter:
# The variable i is initialized with the value 3. This variable will be used to keep track of the countdown.

# While Loop:
# The while loop is used to repeat a block of code as long as the condition specified (i != 0) is true.
# In this case, as long as the value of i is not equal to 0, the loop will continue executing.

# Loop Body:
# Inside the loop, the print("meow") statement is executed. This will print the string "meow" to the console 
# each time the loop iterates.

# Update Counter:
# After each iteration of the loop, the value of i is decremented by 1 using the statement i = i - 1. 
# his reduces the countdown by one.

# Loop Termination:
# The loop will continue to iterate as long as the condition i != 0 remains true.
# Once i reaches 0, the condition becomes false, and the loop terminates.