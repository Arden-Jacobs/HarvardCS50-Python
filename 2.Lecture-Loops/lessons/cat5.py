# cat5.py - Demonstrates a For Loop Using a List

# Use a for loop to iterate through a list
for i in [0, 1, 2]:
    print("meow")



# Explanation:
# This code illustrates the usage of a for loop to iterate through a list and execute a specific action
# for each element in the list.

# For Loop with List:
# The for loop is utilized to traverse through each element in the list [0, 1, 2].
# The variable i takes on the values of the elements in the list during each iteration.

# Loop Body:
# Inside the loop, the print("meow") statement is executed. This results in the string "meow" being printed to
# the console for each element in the list.

# Loop Iteration:
# The loop iterates three times, once for each element in the list [0, 1, 2].
