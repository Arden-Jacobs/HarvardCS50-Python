# cat2.py - Demonstrates a While Loop for Counting Up

# Initialize the counter
i = 1

# Use a while loop to count up
while i <= 3:
    print("meow")
    i = i + 1



# Explanation:
# This code demonstrates the use of a while loop to perform an action multiple times while a condition is met.
# In this case, the loop is used to count up from a starting value of 1.

# Initialize Counter:
# The variable i is initialized with the value 1. This variable will be used to keep track of the counting process.

# While Loop:
# The while loop is used to repeat a block of code as long as the condition specified (i <= 3) is true.
# The loop will continue executing as long as the value of i is less than or equal to 3.

# Loop Body:
# Inside the loop, the print("meow") statement is executed. This will print the string "meow" to the console
# during each iteration of the loop.

# Update Counter:
# After each iteration of the loop, the value of i is incremented by 1 using the statement i = i + 1.
# This increases the count by one.

# Loop Termination:
# The loop will continue to iterate as long as the condition i <= 3 remains true. Once i becomes greater than 3,
# the condition becomes false, and the loop terminates.
